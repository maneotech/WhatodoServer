
const millisecond = 1; // ms
const second = 1000 * millisecond; // ms
const minute = 60 * second; // ms
const hour = 60 * minute; // ms
const day = 24 * hour; // ms

export const DateConstants = {
    millisecond : millisecond,
    second : second,
    minute : minute,
    hour : hour,
    day : day
};